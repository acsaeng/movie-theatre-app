package DataSource;

/**
 * Represents a credit entity
 * @author Rohinesh Ram
 *
 */
public class Credit {
	
	private String email;
	private double amount;
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
	
	public double getAmount() {
		return amount;
	}
	
	public void setAmount(double amount) {
		this.amount = amount;
	}
}
